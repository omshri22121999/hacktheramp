package com.omshri.hacktheramp;

class ColorsClass {
    String name;
    int r;
    int g;
    int b;

    public ColorsClass(String name, int r, int g, int b) {
        this.name = name;
        this.r = r;
        this.g = g;
        this.b = b;
    }

    public String getName() {
        return name;
    }

    public int getR() {
        return r;
    }

    public int getG() {
        return g;
    }

    public int getB() {
        return b;
    }
}
